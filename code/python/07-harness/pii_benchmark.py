@dataclass
class TypeMetrics:
    pii_type: str
    tp: int = 0
    fp: int = 0
    tn: int = 0
    fn: int = 0
    fp_examples: list[str] = field(default_factory=list)
    fn_examples: list[str] = field(default_factory=list)

    @property
    def precision(self) -> float:
        return self.tp / (self.tp + self.fp) if (self.tp + self.fp) > 0 else 0.0

    @property
    def recall(self) -> float:
        return self.tp / (self.tp + self.fn) if (self.tp + self.fn) > 0 else 0.0

    @property
    def f1(self) -> float:
        p, r = self.precision, self.recall
        return 2 * p * r / (p + r) if (p + r) > 0 else 0.0


_PII_TYPES = ["credit_card", "ssn", "email", "phone", "api_key"]


def _run_benchmark(
    pii_inputs: list[TestInput],
    non_pii_inputs: list[TestInput],
    detector: PIIDetector,
) -> dict[str, TypeMetrics]:
    metrics: dict[str, TypeMetrics] = {
        t: TypeMetrics(pii_type=t) for t in _PII_TYPES
    }

    all_inputs = pii_inputs + non_pii_inputs

    for inp in all_inputs:
        detections = detector.detect(inp.text)
        detected_types = {d.type for d in detections}

        for pii_type in _PII_TYPES:
            m = metrics[pii_type]
            expected = pii_type in inp.pii_types

            if expected and pii_type in detected_types:
                m.tp += 1
            elif expected and pii_type not in detected_types:
                m.fn += 1
                m.fn_examples.append(inp.text[:80])
            elif not expected and pii_type in detected_types:
                m.fp += 1
                m.fp_examples.append(inp.text[:80])
            else:
                m.tn += 1

    return metrics


# ---------------------------------------------------------------------------
# Report
# ---------------------------------------------------------------------------


def run_benchmark() -> None:
    """Generate test data, run the detector, and print an accuracy report."""
    pii_inputs = _generate_pii_inputs(500)
    non_pii_inputs = _generate_non_pii_inputs(500)

    detector = PIIDetector()
    metrics = _run_benchmark(pii_inputs, non_pii_inputs, detector)

    # Aggregate totals
    total_tp = sum(m.tp for m in metrics.values())
    total_fp = sum(m.fp for m in metrics.values())
    total_fn = sum(m.fn for m in metrics.values())
    total_tn = sum(m.tn for m in metrics.values())

    overall_precision = total_tp / (total_tp + total_fp) if (total_tp + total_fp) > 0 else 0.0
    overall_recall = total_tp / (total_tp + total_fn) if (total_tp + total_fn) > 0 else 0.0
    overall_f1 = (
        2 * overall_precision * overall_recall / (overall_precision + overall_recall)
        if (overall_precision + overall_recall) > 0
        else 0.0
    )
    # Total non-PII inputs across all types
    total_non_pii_checks = sum(m.fp + m.tn for m in metrics.values())
    fp_rate = total_fp / total_non_pii_checks if total_non_pii_checks > 0 else 0.0
    total_pii_checks = sum(m.tp + m.fn for m in metrics.values())
    fn_rate = total_fn / total_pii_checks if total_pii_checks > 0 else 0.0

    w = 72
    print("\n" + "=" * w)
    print("  PII DETECTION ACCURACY REPORT")
    print("=" * w)
    print(f"Total test inputs  : {len(pii_inputs) + len(non_pii_inputs)}")
    print(f"  With PII         : {len(pii_inputs)}")
    print(f"  Without PII      : {len(non_pii_inputs)}")

    print(f"\n{'BY PII TYPE':}")
    header = (
        f"  {'Type':<14} {'Precision':>9} {'Recall':>7} {'F1 Score':>9} "
        f"{'TP':>5} {'FP':>5} {'FN':>5}"
    )
    print(header)
    print("  " + "-" * (len(header) - 2))

    type_display = {
        "credit_card": "Credit Card",
        "ssn": "SSN",
        "email": "Email",
        "phone": "Phone",
        "api_key": "API Key",
    }
    for pii_type in _PII_TYPES:
        m = metrics[pii_type]
        print(
            f"  {type_display[pii_type]:<14} {m.precision:>9.2f} {m.recall:>7.2f} "
            f"{m.f1:>9.2f} {m.tp:>5} {m.fp:>5} {m.fn:>5}"
        )

    print(f"\n{'OVERALL':}")
    print(f"  Precision           : {overall_precision:.2f}")
    print(f"  Recall              : {overall_recall:.2f}")
    print(f"  F1 Score            : {overall_f1:.2f}")
    print(
        f"  False Positive Rate : {fp_rate * 100:.1f}% "
        f"({total_fp} non-PII inputs flagged)"
    )
    print(
        f"  False Negative Rate : {fn_rate * 100:.1f}% "
        f"({total_fn} PII instances missed)"
    )

    # ── False positives ────────────────────────────────────────────────────
    all_fp: list[tuple[str, str]] = []
    for pii_type, m in metrics.items():
        for ex in m.fp_examples[:3]:
            all_fp.append((pii_type, ex))

    if all_fp:
        print(f"\n{'MOST COMMON FALSE POSITIVES':}")
        seen: dict[str, int] = {}
        for pii_type, ex in all_fp:
            key = textwrap.shorten(ex, width=70)
            seen[key] = seen.get(key, 0) + 1
        for text, count in sorted(seen.items(), key=lambda x: -x[1])[:5]:
            print(f"  - [{count}x] {text}")
    else:
        print(f"\n{'MOST COMMON FALSE POSITIVES':}")
        print("  None detected.")

    # ── False negatives ────────────────────────────────────────────────────
    all_fn: list[tuple[str, str]] = []
    for pii_type, m in metrics.items():
        for ex in m.fn_examples[:3]:
            all_fn.append((pii_type, ex))

    if all_fn:
        print(f"\n{'MOST COMMON FALSE NEGATIVES':}")
        for pii_type, ex in all_fn[:5]:
            print(f"  - [{pii_type}] {textwrap.shorten(ex, width=65)}")
    else:
        print(f"\n{'MOST COMMON FALSE NEGATIVES':}")
        print("  None detected.")

    # ── Examples ───────────────────────────────────────────────────────────
    print(f"\n{'REDACTION EXAMPLES (first 3 PII inputs)':}")
    detector2 = PIIDetector()
    for inp in pii_inputs[:3]:
        redacted, detections = detector2.redact(inp.text)
        print(f"  Original : {inp.text}")
        print(f"  Redacted : {redacted}")
        print(f"  Found    : {[d.type for d in detections]}")
        print()

    print()


if __name__ == "__main__":
    run_benchmark()
